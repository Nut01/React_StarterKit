// Default Parameters

function add(a, b = 1) {
    return a + b
}

/* ถ้าเรียกใช้ add(1) จะได้ผลลัพธ์เป็น 2 เพราะค่า b จะถูกกำหนดเป็น 1 โดย Default Parameters 
แต่ถ้าเรียกใช้ add(1,2) จะได้ผลลัพธ์เป็น 3 เพราะค่า b จะถูกกำหนดเป็น 2 โดยการส่งค่าเข้าไปใน Function */

let [x1, y1 = 1] = [1] // ค่าในตัวแปร x1 จะมีค่า 1 และค่าในตัวแปร y1 จะมีค่า 1 เพราะค่าที่ส่งเข้าไปใน Array มีค่าเพียง 1 ตัวเท่านั้น

